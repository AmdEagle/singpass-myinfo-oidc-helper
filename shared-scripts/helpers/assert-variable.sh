#!/bin/bash

# ==============================================================================
# This script asserts that the variable has been set
# ==============================================================================

# ==============================================================================
# Inputs
# ==============================================================================

VARIABLE_NAME=${1}

# ==============================================================================
# Script
# ==============================================================================

echo "Asserting ${VARIABLE_NAME} exists"

if [ -z "${!VARIABLE_NAME}" ] || [ "${!VARIABLE_NAME}" == "" ]; then
	echo "${VARIABLE_NAME} must be set" >&2
	exit 1
fi
